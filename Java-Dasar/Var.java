public class Var {
    public static void main(String[] args) {

        // Kata Kunci Var
        // • Sejak versi Java 10, Java mendukung pembuatan variabel dengan kata kunci
        // var, sehingga kita tidak perlu menyebutkan tipe datanya
        // • Namun perlu diingat, saat kita menggunakan kata kunci var untuk membuat
        // variable, kita harus menginisiasi value / nilai dari variable tersebut secara
        // langsung

        var firstName = "Daffa";
        var middleName = "Kurniawan";
        var lastName = "Fajriansyah";

        var age = 18;

    }
}
