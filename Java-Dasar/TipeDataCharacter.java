public class TipeDataCharacter {
    public static void main(String[] args) {

        // • Data Character (huruf) di Java direpresentasikan oleh tipe char.
        // • Untuk membuat data char di Java, kita bisa menggunakan tanda ' (petik
        // satu)
        // di awal dan di akhir karakter

        char d = 'D';
        char a = 'A';
        char f = 'F';
        char f2 = 'F';
        char a2 = 'A';

        // Println menghasilkan enter
        System.out.println(d);
        System.out.println(a);
        System.out.println(f);
        System.out.println(f2);
        System.out.println(a2);

        // Print tanpa enter
        System.out.print(d);
        System.out.print(a);
        System.out.print(f);
        System.out.print(f2);
        System.out.print(a2);
    }
}
